# skipping characters of strings..
name = "Naveen012345689"
# print(name[0:10:n]) # Skip n - 1 characters

print(name[0:10:1]) # Skip 0 characters, which means no skipping
print(name[0:10:2]) # Skip 1 character # 2-1 = 1 character skipped
print(name[0:10:3]) # Skip 2 characters # 3-1 = 2 characters skipped 

print(name[:4]) # Replace the first empty number with 0 # name[0:4]
print(name[1:]) # Replace the second empty number with length # name[1:15]

