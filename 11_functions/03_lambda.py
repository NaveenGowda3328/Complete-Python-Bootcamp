square = lambda x: x*x  # lambda function to square a number # short form
''' As good writing as..
    def square(x):     # long form
    return x * x'''
sum = lambda x, y: x + y  # lambda function to sum two numbers # short form
''' As good writing as..
   def sum(x, y):       # long form
   return x + y'''
print(square(5))  # Output: 25
print(sum(3, 4))  # Output: 7