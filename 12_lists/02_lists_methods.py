marks = [2,21,26,35,8,9]
extend_marks = [56,65,34]
print(marks)
marks.append(77) # This will change the oringinal list
print(marks)
marks.pop() # This will remove the element from the end of the list
print(marks)
marks.extend(extend_marks) # This will add the elements of extend_marks to marks
print(marks)
marks.reverse() # This will reverse the list
print(marks)
marks.sort() # This will sort the list in ascending order
print(marks)
marks.remove(2) # This will remove the elements 2 from the list
print(marks)
marks.insert(2, 100) # This will insert 100 at index 2
print(marks)
